#include "../incl/std.h"

void	ft_bzero(void *str, size_t n)
{
	size_t	i;
	char	*dest;

	i = 0;
	dest = (char *)str;
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
}
